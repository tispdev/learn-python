#Create and print a list
fruits = ["apple", "banana", "orange"]
print(fruits)



#access by index
fruits = ["apple", "banana", "orange"]
print(fruits[0]) #first item
print(fruits[2]) #third item


#modify a list
fruits = ["apple", "banana", "orange"]
print(fruits)
fruits[1] = "mango" #change stored values in an index of a list
print(fruits)


#loop through a list
fruits = ["apple", "banana", "orange"]
for i in fruits:
    print("I like", i)
